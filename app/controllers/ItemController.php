	 * Display the user's packing list.
	 *
	 * @return Response
	 */
	public function index($format = 'html')
	{
        $format = str_replace('.', '', $format);;

        $items = Auth::user()->items;
        switch ($format)
        {
          case 'json': return Response::json($items);
          default: return View::make('item.index', array('items' => $items));
        }
	}

